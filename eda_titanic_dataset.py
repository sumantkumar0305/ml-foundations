df['Family_size'] = df['SibSp']+df['Parch']+1

def family_transform(size):

  if size == 1:
    return "Alone"
  elif 2<= size <= 4:
    return "Small"
  else:
    return "Large"

df['Family_type'] = df['Family_size'].apply(family_transform)

df['Surname'] = df['Name'].str.split(',').str.get(0)

df['Title'] = df['Name'].str.split(',').str.get(1).str.strip().str.split(' ').str.get(0)

